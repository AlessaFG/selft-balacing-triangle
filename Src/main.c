#define MPU6050_ADDRESS 0x68 << 1
#define SMPLRT_DIV_REG 0x19
#define GYRO_CONFIG_REG 0x1B
#define ACCEL_CONFIG_REG 0x1C
#define ACCEL_XOUT_H_REG 0x3B
#define GYRO_XOUT_H_REG 0x43
#define PWR_MGMT_1_REG 0x6B
#define WHO_AM_I_REG 0x75

int Ax, Ay, Az,Gx,Gy,Gz;

void MPU6050_Init (void)
{
    uint8_t check;
    uint8_t Data;
    // Check if the sensor is available
    HAL_I2C_Mem_Read (&hi2c1, MPU6050_ADDRESS, WHO_AM_I_REG, 1, &check, 1, HAL_MAX_DELAY);

    if (check == 104) // Checking the return value of register 0x75
    {
        // Write to the power management register to wake up the sensor
        Data = 0;
        HAL_I2C_Mem_Write(&hi2c1, MPU6050_ADDRESS, PWR_MGMT_1_REG, 1, &Data, 1, 1000);

        // Set the sample rate to 1 kHz
        Data = 0x07;
        HAL_I2C_Mem_Write(&hi2c1, MPU6050_ADDRESS, SMPLRT_DIV_REG, 1, &Data, 1, 1000);

        // Configure the accelerometer
        Data = 0x00;
        HAL_I2C_Mem_Write(&hi2c1, MPU6050_ADDRESS, ACCEL_CONFIG_REG, 1, &Data, 1, 1000);

        // Configure the gyroscope
        Data = 0x00;
        HAL_I2C_Mem_Write(&hi2c1, MPU6050_ADDRESS, GYRO_CONFIG_REG, 1, &Data, 1, 1000);
    }
}
void MPU6050_ReadAccelGyro(int16_t* accelData, int16_t* gyroData) {
    uint8_t Rec_Data[14];

    // Read 14 bytes of data starting from 0x3B (ACCEL_XOUT_H)
    HAL_I2C_Mem_Read(&hi2c1, MPU6050_ADDRESS, 0x3B, 1, Rec_Data, 14, HAL_MAX_DELAY);

    // Accelerometer data
    accelData[0] = (int16_t)(Rec_Data[0] << 8 | Rec_Data[1]);
    accelData[1] = (int16_t)(Rec_Data[2] << 8 | Rec_Data[3]);
    accelData[2] = (int16_t)(Rec_Data[4] << 8 | Rec_Data[5]);

    Ax = accelData[0]/16384;
    Ay = accelData[1]/16384;
    Az = accelData[2]/16384;

    // Gyroscope data
    gyroData[0] = (int16_t)(Rec_Data[8] << 8 | Rec_Data[9]);
    gyroData[1] = (int16_t)(Rec_Data[10] << 8 | Rec_Data[11]);
    gyroData[2] = (int16_t)(Rec_Data[12] << 8 | Rec_Data[13]);

    Gx = gyroData[0]/131;
    Gy = gyroData[1]/131;
    Gz = gyroData[2]/131;
}
